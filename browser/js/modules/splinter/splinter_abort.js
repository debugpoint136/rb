Browser.prototype.splinter_abort = function () {
    this.trunk.sethmspan_refresh(this.trunk.hmSpan + this.hmSpan);
    delete horcrux[this.horcrux];
    var h = this.main.parentNode;
    h.parentNode.removeChild(h);
};


